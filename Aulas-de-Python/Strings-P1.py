#Strings P1
curso = "Curso de Python"

print(curso[9:15])

#Lenuma função embutida que retorna o número de elementos em um objeto
print("tamanho: " + str(len(curso)))

#Função strip remove todos os espaços em branco no início e no final de uma string
print(curso.strip())

# retorna a string com todos os caracteres convertidos para minúsculas.
print(curso.lower().strip())

#Funçao upper converte todas as letras minúsculas numa string para maiúsculas
print(curso.upper())